Welcome to Jester!

Jester is VoIP toolkit for FreeSWITCH written in Lua.  The goal of Jester
is to provide a standardized set of tools that allow you to accomplish most of
the common tasks you'll face when putting together phone trees, voicemail
systems, etc.  And, if Jester can't do something you need, it's modular,
extensible design allows you to easily add the functionality in a way that
not only you but others can benefit from!
